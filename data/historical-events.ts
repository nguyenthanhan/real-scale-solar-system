// Combined historical events
export const HISTORICAL_EVENTS: HistoricalEvent[] = [
  ...solarEclipses,
  ...lunarEclipses,
  ...planetaryConjunctions,
  ...spaceMissions,
  ...meteorShowers,
].sort((a, b) => a.date.getTime() - b.date.getTime());

// Helper to get events by category
export function getEventsByCategory(category: string): HistoricalEvent[] {
  if (category === "all") {
    return HISTORICAL_EVENTS;
  }
  return HISTORICAL_EVENTS.filter((event) => event.category === category);
}
